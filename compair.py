# ตัวดำเนินการเปรียบเทียบ

# นำข้อมูลอย่างน้อย 2 ค่ามาเปรียบเทียบ
# ชนิดของข้อมูลต้องเหมือนกัน
# คำตอบมีแค่ จริง/เท็จ

x,y = 10,5

print('ค่า x มากกว่า y หรือไม่ ',x>y)
print('ค่า x น้อยกว่า y หรือไม่ ',x<y)
print('ค่า x เท่ากับ  y หรือไม่ ',x==y)
print('ค่า x ไม่เท่ากับy หรือไม่',x!=y)

print('ค่า x มากกว่า หรือ เท่ากับ y หรือไม่',x>=y)
print('ค่า x น้อบกว่า หรือ เท่ากับ y หรือไม่',x<=y)